    private void prikaziTabeluKlizaca() {

        try {

            List<Klizac> klizaci = Kontroler.getInstance().dajSveKlizace();
            TableModelKlizac tm = new TableModelKlizac(klizaci);
            tblKlizaci.setModel(tm);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da učita listu klizača");
        }
    }

    private int getSelectedRow() {
        int selectedRow = tblKlizaci.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Niste odabrali red!",
                    "Poruka", JOptionPane.INFORMATION_MESSAGE);
        }
        return selectedRow;
    }

    public void obrisiKlizaca(Klizac klizac) throws Exception {

        TableModelKlizac tmk = (TableModelKlizac) tblKlizaci.getModel();
        TransferObjekatZahtev toz = new TransferObjekatZahtev();
        toz.setOperacija(Util.OPERACIJA_OBRISI_KLIZACA);
        toz.setParametar(klizac);
        Komunikacija.getInstanca().posaljiZahtev(toz);
        TransferObjekatOdgovor too = Komunikacija.getInstanca().primiOdgovor();
        if (too.getStatus() == EnumStatus.SVE_U_REDU) {
            JOptionPane.showMessageDialog(this,
                    "Sistem je obrisao klizača", "Poruka", JOptionPane.INFORMATION_MESSAGE);
            tmk.obrisiKlizaca(klizac);
        } else {
            JOptionPane.showMessageDialog(this,
                    "Sistem nije obrisao klizača", "Poruka", JOptionPane.INFORMATION_MESSAGE);
            tmk.obrisiKlizaca(klizac);
        }

    }

    private void postaviFormu(FormMode formMode) {

        switch (formMode) {
            case DODAJ_UCESNIKE:

                filterPrezime.setVisible(false);
                txtPrezime.setVisible(false);
                btnDetalji.setVisible(false);
                txtOdabirKliazaca.setText("Odaberite učesnike na takmičenju: ");
                btnDodaj.setVisible(true);
                break;

            case PRETRAZIVANJE_KLIZACA:
                btnDetalji.setVisible(false);
                filterPrezime.setVisible(true);
                txtPrezime.setVisible(true);
                btnDetalji.setVisible(true);
                txtOdabirKliazaca.setVisible(false);
                btnDodaj.setVisible(false);
                break;

        }
    }

    private void sakrijDugme() {
        btnDodaj.setVisible(false);
    }

    public void filtrirajPoPrezimenu() {

        String prezime = txtPrezime.getText();

        Klizac k = new Klizac();
        k.setPrezime(prezime);
        
        if (prezime.isEmpty() || prezime.length() > 1) {
            JOptionPane.showMessageDialog(this, "Unesite slovo!");
            return;
        }

        lblPoruka.setVisible(false);

        if (filterPrezime.isSelected()) {

            lblPoruka.setVisible(false);
            ArrayList<Klizac> klizaci = Kontroler.getInstance().vratiKlizace(k);
            TableModelKlizac tabela = new TableModelKlizac(klizaci);
            tblKlizaci.setModel(tabela);

            if (klizaci.size() < 1) {
                lblPoruka.setVisible(true);
                lblPoruka.setText("Sistem ne može da nađe klizače po zadatoj vrednosti!");
                return;
            } else {
                lblPoruka.setVisible(true);
                lblPoruka.setText("Sistem je našao klizače po zadatoj vrednosti!");
                return;
            }

        } else {

            List<Klizac> klizaci = Kontroler.getInstance().dajSveKlizace();
            TableModelKlizac tabela = new TableModelKlizac(klizaci);
            tblKlizaci.setModel(tabela);
        }
    }

