    private void btnDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljiActionPerformed

        try {

            int odabraniRed = getSelectedRow();

            if (odabraniRed != -1) {

                String klizacID = (String) tblKlizaci.getValueAt(odabraniRed, 0);
                JOptionPane.showMessageDialog(this,
                        "ID odabranog klizača: " + klizacID, "Poruka",
                        JOptionPane.INFORMATION_MESSAGE);

                Session.getInstance().setCurrentUseCase(UseCase.UC_PRETRAZIVANJE_KLIZACA);
                Session.getInstance().getUseCaseParams().clear();
                Session.getInstance().getUseCaseParams().put("klizacID", klizacID);

                this.dispose();

                JDialog form
                        = new FormaKreirajKlizaca(null, true, FormMode.VIDI_KLIZACA);
                form.setLocationRelativeTo(null);
                form.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this,
                        "Odaberite red!", "Poruka",
                        JOptionPane.WARNING_MESSAGE);
                return;
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "Sistem ne može da prikaže klizača!", "Poruka",
                    JOptionPane.INFORMATION_MESSAGE);
            ex.printStackTrace();
        }

    }//GEN-LAST:event_btnDetaljiActionPerformed

