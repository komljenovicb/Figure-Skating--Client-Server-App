    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed

        postaviFormu(FormMode.DODAJ_UCESNIKE);

        int red = tblKlizaci.getSelectedRow();

        String id = (String) tblKlizaci.getValueAt(red, 0);
        String ime = (String) tblKlizaci.getValueAt(red, 1);
        String prezime = (String) tblKlizaci.getValueAt(red, 2);
        Date datum = (Date) tblKlizaci.getValueAt(red, 3);
        EnumPol pol = (EnumPol) tblKlizaci.getValueAt(red, 4);
        Drzava drzava = (Drzava) tblKlizaci.getValueAt(red, 5);

        Kontroler.getInstance().pronadjiDrzavu(drzava.getDrzavaID());

        Klizac k = new Klizac(id, ime, prezime, datum, pol, drzava);

        FrmKreirajTakmicenje topFrame = (FrmKreirajTakmicenje) getParent();

        topFrame.dodajKlizacaUTabeluUcesca(k);


    }//GEN-LAST:event_btnDodajActionPerformed

