    private void prikaziTabeluTakmicenja() {

        ArrayList<Takmicenje> takmicenja = (ArrayList<Takmicenje>) Kontroler.getInstance().ucitajTakmicenja();
        TableModelTakmicenje tmt = new TableModelTakmicenje(takmicenja);
        tblTakmicenje.setModel(tmt);

    }

    public void filtrirajPoDatumu() {

        Date datOd = this.datumOd.getDate();
        Date datDo = this.datumDo.getDate();

        if (datOd == null || datDo == null) {
            JOptionPane.showMessageDialog(this, "Odaberite datum!!");
            return;
        }

        Takmicenje t = new Takmicenje();
        t.setPocetak(datOd);
        t.setKraj(datDo);

        txtPoruka.setVisible(false);
        lblOdaberi.setVisible(false);

        if (filter.isSelected()) {

            if (datOd.after(datDo)) {
                JOptionPane.showMessageDialog(this, "Datum početka mora biti pre datuma kraja!");
                return;
            }
   
            ArrayList<Takmicenje> takmicenja = Kontroler.getInstance().ucitajTakmicenja(t);
            TableModelTakmicenje tmt = new TableModelTakmicenje(takmicenja);
            tblTakmicenje.setModel(tmt);;
            lblOdaberi.setVisible(false);
            txtPoruka.setVisible(false);

            if (takmicenja.size() < 1) {
                txtPoruka.setVisible(true);
                txtPoruka.setText("Sistem ne može da nađe takmičenja po zadatoj vrednosti!");
                return;
            } else {
                // btnPronadji.setVisible(true);
                lblOdaberi.setVisible(true);
                lblOdaberi.setText("Odaberite takmičenje iz liste!");
                txtPoruka.setVisible(true);
                txtPoruka.setText("Sistem je našao takmičenja po zadatoj vrednosti!");
                return;
            }
        } else {

            List<Takmicenje> takmicenja = Kontroler.getInstance().ucitajTakmicenja();
            TableModelTakmicenje tmt = new TableModelTakmicenje(takmicenja);
            tblTakmicenje.setModel(tmt);
        }
    }

