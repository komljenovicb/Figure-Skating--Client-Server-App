    private void btnPronadjiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPronadjiActionPerformed

        int odabraniRed = tblTakmicenje.getSelectedRow();

        if (odabraniRed == -1) {
            JOptionPane.showMessageDialog(this, "Morate odabrati red!");
            return;
        }

        String naziv = (String) tblTakmicenje.getValueAt(odabraniRed, 0);

        JOptionPane.showMessageDialog(this,
                "Naziv takmičenja: " + naziv, "Poruka",
                JOptionPane.INFORMATION_MESSAGE);

        List<Takmicenje> takmicenja = Kontroler.getInstance().ucitajTakmicenja();

        for (Takmicenje t : takmicenja) {
            if (t.getNaziv().equals(naziv)) {
                Kontroler.getInstance().setTakmicenje(t);
                this.dispose();
                
                
                ArrayList<Ucesce> ucesca 
                        = (ArrayList<Ucesce>) Kontroler.getInstance().vratiUcescaNaTakmicenju(t);
                t.setUcesca(ucesca);
                
                // Unos novog ucesca ili unos rezultata za postojece i odredjivanje plasmana
                
                if (t.getUcesca().isEmpty()) {
                    
                    FrmKreirajTakmicenje fk
                            = new FrmKreirajTakmicenje(FormMode.VIDI_TAKMICENJE);
                    fk.vidiTakmicenje(t);
                    fk.setLocationRelativeTo(null);
                    fk.setVisible(true);
                } else {
                    
                    FrmKreirajTakmicenje fk
                            = new FrmKreirajTakmicenje(FormMode.UNESI_REZULTATE);
                    fk.unesiRezultateZaTakmicenje(t);
                    fk.setLocationRelativeTo(null);
                    fk.setVisible(true);
                }
                return;
            }
        }

        FrmKreirajTakmicenje form = new FrmKreirajTakmicenje(FormMode.VIDI_TAKMICENJE);
        form.setLocationRelativeTo(null);
        form.setVisible(true);

    }//GEN-LAST:event_btnPronadjiActionPerformed

